"""Cross-validation: Python :representative kinematics vs the REAL kami-genesis solver.

ADR-2606091800 promised the Python `PlanarArm` FK/IK contract is "the subset the
cells depend on, so the swap [to the kami-engine Featherstone solver] is
mechanical". This suite PROVES it against a golden trace computed by the actual
Rust solver (kami-genesis `Articulation3dConfig`, Featherstone, URDF-loaded):

  golden/kami_fk_ik_trace.json   — generated by kami-bridge/ (see its README);
                                   regenerate after a kami-engine bump with
        cargo run --manifest-path kami-bridge/Cargo.toml --release \
            > golden/kami_fk_ik_trace.json

What is asserted:
  * FK parity — Python PlanarArm((1.2,1.0)).fk(q) equals the Rust Featherstone
    tip for every grid sample (the exact arm geometry the domain actors use).
  * IK cross-solver agreement — where the Rust damped-least-squares IK converged,
    the Rust joint solution maps onto the target under PYTHON FK, and the Python
    analytic ik2 solves the same target exactly.
  * Analytic-IK robustness — Python ik2 solves EVERY reachable golden target,
    including any the iterative Rust DLS missed (analytic 2-link IK is closed
    form; DLS can stall on far seeds — a known, recorded asymmetry).
  * giemon_arm6 readiness — the 6-DOF swap target loads with 6 DOF, its FK tips
    stay inside the workspace bound, and its position-IK converges (<1mm) on
    reachable targets: the Rust side of the future swap is live and sane.

Run:
    cd 20-actors/kuni-umi/robotics
    PYTEST_DISABLE_PLUGIN_AUTOLOAD=1 python3 -m pytest test_kami_parity.py
"""

from __future__ import annotations

import json
import math
import pathlib

import pytest

from kinematics import PlanarArm

GOLDEN = pathlib.Path(__file__).resolve().parent / "golden" / "kami_fk_ik_trace.json"

# f32 trace vs f64 Python: tip coordinates agree to f32 precision.
FK_TOL = 5e-4
# Rust DLS IK is "converged" on a target when its self-reported error is small.
RUST_IK_CONVERGED = 1e-3

ARM = PlanarArm(link_lengths=(1.2, 1.0))


@pytest.fixture(scope="module")
def trace() -> dict:
    assert GOLDEN.exists(), (
        f"golden trace missing at {GOLDEN}; regenerate with "
        "`cargo run --manifest-path kami-bridge/Cargo.toml --release "
        "> golden/kami_fk_ik_trace.json` (requires the 40-engine/kami-engine submodule)"
    )
    return json.loads(GOLDEN.read_text())


# ─── 2-link parity: the exact PlanarArm contract ─────────────────────

def test_golden_geometry_matches_python_arm(trace):
    p = trace["parity_2link"]
    assert tuple(p["links"]) == ARM.link_lengths
    assert p["fk"], "empty FK grid"


def test_fk_parity_full_grid(trace):
    """Python FK == Rust Featherstone FK at every grid sample."""
    for s in trace["parity_2link"]["fk"]:
        pose = ARM.fk(tuple(s["q"]))
        rx, ry, rz = s["tip"]
        assert rz == pytest.approx(0.0, abs=1e-6), "planar arm left the x-y plane"
        assert pose.x == pytest.approx(rx, abs=FK_TOL), f"x mismatch at q={s['q']}"
        assert pose.y == pytest.approx(ry, abs=FK_TOL), f"y mismatch at q={s['q']}"


def test_rust_ik_solutions_verify_under_python_fk(trace):
    """Where Rust DLS converged, its joint solution hits the target under Python FK."""
    converged = [s for s in trace["parity_2link"]["ik"] if s["tip_err"] < RUST_IK_CONVERGED]
    assert len(converged) >= 4, "too few converged Rust IK samples to cross-validate"
    for s in converged:
        tx, ty, _ = s["target"]
        pose = ARM.fk(tuple(s["q"]))
        err = math.hypot(pose.x - tx, pose.y - ty)
        assert err < 2e-3, f"Rust IK solution misses target under Python FK: {err}"


def test_python_analytic_ik_solves_every_reachable_golden_target(trace):
    """Analytic ik2 solves ALL golden targets — including any the DLS stalled on."""
    for s in trace["parity_2link"]["ik"]:
        tx, ty, _ = s["target"]
        assert ARM.reachable(tx, ty), f"golden IK target {s['target']} outside annulus"
        for elbow_up in (True, False):
            q = ARM.ik2(tx, ty, elbow_up=elbow_up)
            assert q is not None
            pose = ARM.fk(q)
            assert math.hypot(pose.x - tx, pose.y - ty) < 1e-6


def test_rust_fk_tips_respect_python_reachability(trace):
    """Every Rust FK tip lies inside the Python arm's reachability annulus.

    Tolerance is FK_TOL: an f32 trace of a fully-extended arm can land a few
    1e-4 outside the f64 annulus boundary (|tip| = 2.2002 vs max_reach 2.2).
    """
    for s in trace["parity_2link"]["fk"]:
        rx, ry, _ = s["tip"]
        r = math.hypot(rx, ry)
        assert ARM.min_reach - FK_TOL <= r <= ARM.max_reach + FK_TOL, (
            f"Rust tip {s['tip']} (r={r}) outside Python annulus "
            f"[{ARM.min_reach}, {ARM.max_reach}] ± {FK_TOL}"
        )


# ─── giemon_arm6: the 6-DOF swap target is live ──────────────────────

def test_giemon_arm6_is_six_dof_with_limits(trace):
    g = trace["giemon_arm6"]
    assert g["ndof"] == 6
    assert len(g["joint_limits"]) == 6
    for lo, hi in g["joint_limits"]:
        assert lo < 0 < hi


def test_giemon_fk_tips_finite_and_inside_workspace(trace):
    g = trace["giemon_arm6"]
    bound = g["max_reach_upper_bound"] + 1e-3
    assert g["fk"], "empty giemon FK grid"
    for s in g["fk"]:
        tip = s["tip"]
        assert all(math.isfinite(c) for c in tip)
        assert math.sqrt(sum(c * c for c in tip)) <= bound, (
            f"giemon tip {tip} outside workspace bound {bound}"
        )


def test_giemon_position_ik_converges_on_reachable_targets(trace):
    g = trace["giemon_arm6"]
    assert g["ik"], "no giemon IK samples"
    for s in g["ik"]:
        assert s["tip_err"] < 1e-3, (
            f"giemon position-IK did not converge on {s['target']}: err={s['tip_err']}"
        )
        assert len(s["q"]) == 6
        # The solver clamps to URDF limits; the solution must respect them.
        for qi, (lo, hi) in zip(s["q"], g["joint_limits"]):
            assert lo - 1e-6 <= qi <= hi + 1e-6
